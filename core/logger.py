"""core/logger.py ‚ÄĒ Colored terminal logger"""
import sys
from datetime import datetime

COLORS = {
    "BOT":   "\033[95m",   # magenta
    "GEN":   "\033[94m",   # blue
    "HUNT":  "\033[96m",   # cyan
    "SCORE": "\033[93m",   # yellow
    "ALERT": "\033[91m",   # red
    "DB":    "\033[92m",   # green
    "SCHED": "\033[90m",   # gray
    "WARN":  "\033[33m",   # orange
    "INFO":  "\033[37m",   # white
}
RESET = "\033[0m"
BOLD  = "\033[1m"

def log(tag: str, msg: str):
    color = COLORS.get(tag, "\033[37m")
    ts = datetime.now().strftime("%H:%M:%S")
    print(f"{COLORS['INFO']}{ts}{RESET}  {color}{BOLD}[{tag:5}]{RESET}  {msg}")
    sys.stdout.flush()

